    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     int id = Integer.parseInt(tblTorneo.getValueAt(tblTorneo.getSelectedRow(),0).toString());
     
     if(f.eliminarTorneo(id)){
         javax.swing.JOptionPane.showMessageDialog(null, "Torneo eliminado!!");
          f.actualizarTabla(tblTorneo,f.getTorneosActivos());
     }else{
         javax.swing.JOptionPane.showMessageDialog(null, "Torneo eliminado!!","ERROR",0);
          f.actualizarTabla(tblTorneo,f.getTorneosActivos());
     }
    }//GEN-LAST:event_jButton4ActionPerformed

   

