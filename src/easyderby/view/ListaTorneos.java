    private void tblTorneoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblTorneoKeyPressed
       if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
           idTorneo =0;
            setVisible(false);                 
        }   
       if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
           idTorneo =Integer.parseInt(tblTorneo.getValueAt(tblTorneo.getSelectedRow(), 0).toString());
           setVisible(false);                         
            
        }
       
// TODO add your handling code here:
    }//GEN-LAST:event_tblTorneoKeyPressed

