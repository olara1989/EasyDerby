    private void tblPartidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPartidosMouseClicked

        num_p = Integer.parseInt(tblPartidos.getValueAt(tblPartidos.getSelectedRow(), 0).toString());
        ArrayList<String[]> partido = f.getPartido(num_p);
        txtNombre.setText(partido.get(0)[1]);
        txtLugar.setText(partido.get(0)[2]);
        btnGuardar.setText("Modificar");
        guardar = false;
    }//GEN-LAST:event_tblPartidosMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (num_p != 0) {
            int opc = JOptionPane.showConfirmDialog(null, "¿Desear eliminar este partido?");
            if (opc == JOptionPane.OK_OPTION) {
                f.eliminarPartido(num_p);
                JOptionPane.showMessageDialog(null, "Partido eliminado con exito", "OK", JOptionPane.DEFAULT_OPTION);
                f.actualizarTabla(tblPartidos, f.getPartidos());
                num_p = 0;
                txtNombre.requestFocus();
                txtLugar.setText("");
                txtNombre.setText("");
                btnGuardar.setText("Guardar");
                guardar = true;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar un partido", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


