    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (guardar) {
            String n = txtNombre.getText();
            if (!n.equals("")) {
                String l = txtLugar.getText();
                f.agregarPartido(n, l);

                JOptionPane.showMessageDialog(null, "Partido guardado con exito", "OK", JOptionPane.DEFAULT_OPTION);
                f.actualizarTabla(tblPartidos, f.getPartidos());
                txtLugar.setText("");
                txtNombre.setText("");
                txtNombre.requestFocus();
                btnGuardar.setText("Guardar");
                guardar = true;
                num_p = 0;
            } else {
                JOptionPane.showMessageDialog(null, "Debes de colocar un nombre", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            String n = txtNombre.getText();
            String l = txtLugar.getText();
            f.modificarPartido(num_p, n, l);
            JOptionPane.showMessageDialog(null, "Partido modificado con exito", "OK", JOptionPane.DEFAULT_OPTION);
            f.actualizarTabla(tblPartidos, f.getPartidos());
            txtLugar.setText("");
            txtNombre.setText("");
            txtNombre.requestFocus();
            btnGuardar.setText("Guardar");
            guardar = true;
            num_p = 0;
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

